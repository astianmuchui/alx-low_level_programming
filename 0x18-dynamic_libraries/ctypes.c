#include "ctypes.h"

/**
* add - adds two numbers
* @a: first number
* @b: second number
* Return: sum of a and b 
*/

int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtracts two numbers
* @a: first number
* @b: second number
* Return: difference of a and b
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiplies two numbers
* @a: first number
* @b: second number
* Return: product of a and b
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divides two numbers
* @a: first number
* @b: second number
* Return: quotient of a and b
*/

int div(int a, int b)
{
	return (a / b);
}

/**
* mod - finds modulus
* @a: first number
* @b: second number
* Return: a modulo b
*/

int mod(int a, int b)
{
	return (a % b);
}